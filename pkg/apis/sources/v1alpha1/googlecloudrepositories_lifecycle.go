package v1alpha1

import (
	"k8s.io/apimachinery/pkg/runtime/schema"

	"knative.dev/pkg/apis"
	duckv1 "knative.dev/pkg/apis/duck/v1"
)

// GetGroupVersionKind implements kmeta.OwnerRefable.
func (*GoogleCloudRepositoriesSource) GetGroupVersionKind() schema.GroupVersionKind {
	return SchemeGroupVersion.WithKind("GoogleCloudRepositoriesSource")
}

// GetConditionSet implements duckv1.KRShaped.
func (*GoogleCloudRepositoriesSource) GetConditionSet() apis.ConditionSet {
	return googleCloudRepositoriesSourceConditionSet
}

// GetStatus implements duckv1.KRShaped.
func (s *GoogleCloudRepositoriesSource) GetStatus() *duckv1.Status {
	return &s.Status.Status
}

// GetSink implements EventSource.
func (s *GoogleCloudRepositoriesSource) GetSink() *duckv1.Destination {
	return &s.Spec.Sink
}

// GetStatusManager implements EventSource.
func (s *GoogleCloudRepositoriesSource) GetStatusManager() *EventSourceStatusManager {
	return &EventSourceStatusManager{
		ConditionSet:      s.GetConditionSet(),
		EventSourceStatus: &s.Status.EventSourceStatus,
	}
}

// AsEventSource implements EventSource.
func (s *GoogleCloudRepositoriesSource) AsEventSource() string {
	return s.Spec.Repository.String()
}

// Supported event types
const (
	GoogleCloudRepositoriesGenericEventType = "com.google.cloud.repositories.notification"
)

// GetEventTypes returns the event types generated by the source.
func (*GoogleCloudRepositoriesSource) GetEventTypes() []string {
	return []string{
		GoogleCloudRepositoriesGenericEventType,
	}
}

// Status conditions
const (
	// GoogleCloudRepositoriesConditionSubscribed has status True when the source has subscribed to a topic.
	GoogleCloudRepositoriesConditionSubscribed apis.ConditionType = "Subscribed"
)

// googleCloudRepositorieseSourceConditionSet is a set of conditions for
// GoogleCloudRepositoriesSource objects.
var googleCloudRepositoriesSourceConditionSet = NewEventSourceConditionSet(
	GoogleCloudRepositoriesConditionSubscribed,
)

// MarkSubscribed sets the Subscribed condition to True.
func (s *GoogleCloudRepositoriesSourceStatus) MarkSubscribed() {
	googleCloudRepositoriesSourceConditionSet.Manage(s).MarkTrue(GoogleCloudRepositoriesConditionSubscribed)
}

// MarkNotSubscribed sets the Subscribed condition to False with the given
// reason and message.
func (s *GoogleCloudRepositoriesSourceStatus) MarkNotSubscribed(reason, msg string) {
	s.Topic = nil
	s.Subscription = nil
	googleCloudRepositoriesSourceConditionSet.Manage(s).MarkFalse(GoogleCloudRepositoriesConditionSubscribed, reason, msg)
}
